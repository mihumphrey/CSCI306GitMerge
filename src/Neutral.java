public class Neutral {
    public void feedback(int score) {
        if (score == 100)
            System.out.println("Not bad");
        else if (score > 90)
            System.out.println("not bad");
        else if (score > 60)
            System.out.println("not bad");
        else if (score > 50)
            System.out.println("not bad");
        else
            System.out.println("not bad");
    }
}
